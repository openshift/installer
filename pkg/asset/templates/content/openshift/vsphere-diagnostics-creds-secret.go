package openshift

import (
	"context"
	"os"
	"path/filepath"

	"github.com/openshift/installer/pkg/asset"
	"github.com/openshift/installer/pkg/asset/templates/content"
)

const (
	vsphereDiagnosticsCredsSecretFileName = "vsphere-diagnostics-creds-secret.yaml.template"
)

var _ asset.WritableAsset = (*VSphereDiagnosticsCredsSecret)(nil)

// VSphereDiagnosticsCredsSecret is the constant to represent contents of corresponding yaml file
type VSphereDiagnosticsCredsSecret struct {
	FileList []*asset.File
}

// Dependencies returns all of the dependencies directly needed by the asset
func (t *VSphereDiagnosticsCredsSecret) Dependencies() []asset.Asset {
	return []asset.Asset{}
}

// Name returns the human-friendly name of the asset.
func (t *VSphereDiagnosticsCredsSecret) Name() string {
	return "VSphereDiagnosticsCredsSecret"
}

// Generate generates the actual files by this asset
func (t *VSphereDiagnosticsCredsSecret) Generate(_ context.Context, parents asset.Parents) error {
	fileName := vsphereDiagnosticsCredsSecretFileName
	data, err := content.GetOpenshiftTemplate(fileName)
	if err != nil {
		return err
	}
	t.FileList = []*asset.File{
		{
			Filename: filepath.Join(content.TemplateDir, fileName),
			Data:     []byte(data),
		},
	}
	return nil
}

// Files returns the files generated by the asset.
func (t *VSphereDiagnosticsCredsSecret) Files() []*asset.File {
	return t.FileList
}

// Load returns the asset from disk.
func (t *VSphereDiagnosticsCredsSecret) Load(f asset.FileFetcher) (bool, error) {
	file, err := f.FetchByName(filepath.Join(content.TemplateDir, vsphereDiagnosticsCredsSecretFileName))
	if err != nil {
		if os.IsNotExist(err) {
			return false, nil
		}
		return false, err
	}
	t.FileList = []*asset.File{file}
	return true, nil
}
