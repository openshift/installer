package bootkube

import (
	"os"
	"path/filepath"

	"github.com/openshift/installer/pkg/asset"
	"github.com/openshift/installer/pkg/asset/templates/content"
)

const (
	clusterProfileFileName = "cluster-profile.yaml.template"
)

var _ asset.WritableAsset = (*ClusterProfile)(nil)

// ClusterProfile is an asset that generates the cluster-profile.yaml.template file.
type ClusterProfile struct {
	FileList []*asset.File
}

// Dependencies returns all of the dependencies directly needed by the asset
func (t *ClusterProfile) Dependencies() []asset.Asset {
	return []asset.Asset{}
}

// Name returns the human-friendly name of the asset.
func (t *ClusterProfile) Name() string {
	return "ClusterProfile"
}

// Generate generates the actual files by this asset
func (t *ClusterProfile) Generate(parents asset.Parents) error {
	fileName := clusterProfileFileName
	data, err := content.GetBootkubeTemplate(fileName)
	if err != nil {
		return err
	}
	t.FileList = []*asset.File{
		{
			Filename: filepath.Join(content.TemplateDir, fileName),
			Data:     []byte(data),
		},
	}
	return nil
}

// Files returns the files generated by the asset.
func (t *ClusterProfile) Files() []*asset.File {
	return t.FileList
}

// Load returns the asset from disk.
func (t *ClusterProfile) Load(f asset.FileFetcher) (bool, error) {
	file, err := f.FetchByName(filepath.Join(content.TemplateDir, clusterProfileFileName))
	if err != nil {
		if os.IsNotExist(err) {
			return false, nil
		}
		return false, err
	}
	t.FileList = []*asset.File{file}
	return true, nil
}
