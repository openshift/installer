package machine

import (
	"context"
	"encoding/json"
	"fmt"
	"os"

	igntypes "github.com/coreos/ignition/v2/config/v3_2/types"
	"github.com/pkg/errors"
	"github.com/sirupsen/logrus"
	"k8s.io/utils/ptr"

	"github.com/openshift/installer/pkg/asset"
	"github.com/openshift/installer/pkg/asset/ignition"
	"github.com/openshift/installer/pkg/asset/installconfig"
	"github.com/openshift/installer/pkg/asset/tls"
	"github.com/openshift/installer/pkg/types"
	"github.com/openshift/installer/pkg/types/azure"
)

const (
	masterIgnFilename = "master.ign"
)

// Master is an asset that generates the ignition config for master nodes.
type Master struct {
	Config *igntypes.Config
	File   *asset.File
}

var _ asset.WritableAsset = (*Master)(nil)

// Dependencies returns the assets on which the Master asset depends.
func (a *Master) Dependencies() []asset.Asset {
	return []asset.Asset{
		&installconfig.InstallConfig{},
		&tls.RootCA{},
	}
}

// Generate generates the ignition config for the Master asset.
func (a *Master) Generate(_ context.Context, dependencies asset.Parents) error {
	installConfig := &installconfig.InstallConfig{}
	rootCA := &tls.RootCA{}
	dependencies.Get(installConfig, rootCA)

	a.Config = pointerIgnitionConfig(installConfig.Config, rootCA.Cert(), "master")

	if installConfig.Config.Platform.Name() == azure.Name {
		logrus.Debugf("Adding /var partition to skip CoreOS growfs step")
		// See https://issues.redhat.com/browse/OCPBUGS-43625
		ignition.AppendVarPartition(a.Config)
	}

	if installConfig.Config.ControlPlane != nil {
		for i, d := range installConfig.Config.ControlPlane.DataDisks {
			if d.Type == types.Etcd {

				// platform specific...
				if installConfig.Config.ControlPlane.Platform.Azure != nil {
					azurePlatform := installConfig.Config.ControlPlane.Platform.Azure
					device := fmt.Sprintf("/dev/disk/azure/scsi1/lun%d", azurePlatform.DataDisks[i].Lun)

					AddEtcdDisk(a.Config, device)
				}
			}
		}
	}

	data, err := ignition.Marshal(a.Config)
	if err != nil {
		return errors.Wrap(err, "failed to marshal Ignition config")
	}
	a.File = &asset.File{
		Filename: masterIgnFilename,
		Data:     data,
	}

	return nil
}

// Name returns the human-friendly name of the asset.
func (a *Master) Name() string {
	return "Master Ignition Config"
}

// Files returns the files generated by the asset.
func (a *Master) Files() []*asset.File {
	if a.File != nil {
		return []*asset.File{a.File}
	}
	return []*asset.File{}
}

// Load returns the master ignitions from disk.
func (a *Master) Load(f asset.FileFetcher) (found bool, err error) {
	file, err := f.FetchByName(masterIgnFilename)
	if err != nil {
		if os.IsNotExist(err) {
			return false, nil
		}
		return false, err
	}

	config := &igntypes.Config{}
	if err := json.Unmarshal(file.Data, config); err != nil {
		return false, errors.Wrapf(err, "failed to unmarshal %s", masterIgnFilename)
	}

	a.File, a.Config = file, config
	return true, nil
}

func AddEtcdDisk(config *igntypes.Config, device string) {
	config.Storage.Disks = append(config.Storage.Disks, igntypes.Disk{
		Device: device,
		Partitions: []igntypes.Partition{{
			Label:    ptr.To("etcddisk"),
			StartMiB: ptr.To(0),
			SizeMiB:  ptr.To(0),
		}},
		WipeTable: ptr.To(true),
	})

	config.Storage.Filesystems = append(config.Storage.Filesystems, igntypes.Filesystem{
		Device:         "/dev/disk/by-partlabel/etcddisk",
		Format:         ptr.To("xfs"),
		Label:          ptr.To("etcdpart"),
		MountOptions:   []igntypes.MountOption{"defaults", "prjquota"},
		Path:           ptr.To("/var/lib/etcd"),
		WipeFilesystem: ptr.To(true),
	})
	config.Systemd.Units = append(config.Systemd.Units, igntypes.Unit{
		Name:    "var-lib-etcd.mount",
		Enabled: ptr.To(true),
		Contents: ptr.To(`
[Unit]
Requires=systemd-fsck@dev-disk-by\x2dpartlabel-var.service
After=systemd-fsck@dev-disk-by\x2dpartlabel-var.service

[Mount]
Where=/var/lib/etcd
What=/dev/disk/by-partlabel/etcddisk
Type=xfs
Options=defaults,prjquota

[Install]
RequiredBy=local-fs.target
`),
	})
}
